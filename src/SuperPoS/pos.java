    private void txtprdctcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtprdctcodeKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String name = txtprdctcode.getText();
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                conn =DriverManager.getConnection("jdbc:mysql://localhost/superpos", "root", "Emmambu6&");                
                pst = conn.prepareStatement("select * from product where barcode=?");                
                pst.setString(1, name);
                rs = pst.executeQuery();
                
                if(rs.next()==false){
                    JOptionPane.showMessageDialog(this, "Barcode not found");
                }else{
                    String producname = rs.getString("product");
                    String productprice = rs.getString("retail_price");
                    
                    txtPrdname.setText(producname.trim());
                    txtprice.setText(productprice.trim());
                }
                
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            }          
        }
    }//GEN-LAST:event_txtprdctcodeKeyPressed

