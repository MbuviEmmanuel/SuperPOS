    private void pos(){
         String name = txtprdctcode.getText();
            
        try {
            Class.forName("com.mysql.jdbc.Driver");
             conn =DriverManager.getConnection("jdbc:mysql://localhost/superpos", "root", "Emmambu6&");                
              pst = conn.prepareStatement("select * from product where barcode=?");                
              pst.setString(1, name);
              rs = pst.executeQuery();
              
              while(rs.next()){
                  int currentqty;
                 currentqty = rs.getInt("qty");
                
                  int price = Integer.parseInt(txtprice.getText());
                  int qtynew = Integer.parseInt(txtqty.getText());
                
                  int tot = price * qtynew;
                  
                  if(qtynew >= currentqty){
                      JOptionPane.showMessageDialog(this, "Available Product" + " =" + currentqty);
                      JOptionPane.showMessageDialog(this, "Quantity not enough");
                      
                  }else{
                       model = ( DefaultTableModel)jTable1.getModel();
                       model.addRow(new Object[]{
                          txtprdctcode.getText(),
                           txtPrdname.getText(),
                           txtprice.getText(),
                           txtqty.getText(), 
                            tot,   
                       });
                       
                   int sum= 0;
                   for(int i=0; i<jTable1.getRowCount(); i++){
                       sum = sum + Integer.parseInt(jTable1.getValueAt(i,4).toString());
                   }
                   
                   txtsub.setText(Integer.toString(sum));
                       
                  txtprdctcode.setText("");
                  txtPrdname.setText("");
                   txtprice.setText("");
                   txtqty.setText("");
                   txtprdctcode.requestFocus();
                  }
              }
              
              
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        }
               
    }
    
    
